<?php

namespace App\Entity;

use App\Repository\PaiementRepository;
use Doctrine\ORM\Mapping as ORM;

#[ORM\Entity(repositoryClass: PaiementRepository::class)]
class Paiement
{
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column]
    private ?int $id = null;

    #[ORM\OneToOne(cascade: ['persist', 'remove'])]
    private ?Commande $id_commande = null;

    #[ORM\Column(length: 255)]
    private ?string $methode = null;

    #[ORM\Column]
    private ?\DateTimeImmutable $cerated_At = null;

    #[ORM\Column(length: 255)]
    private ?string $montant = null;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getIdCommande(): ?Commande
    {
        return $this->id_commande;
    }

    public function setIdCommande(?Commande $id_commande): static
    {
        $this->id_commande = $id_commande;

        return $this;
    }

    public function getMethode(): ?string
    {
        return $this->methode;
    }

    public function setMethode(string $methode): static
    {
        $this->methode = $methode;

        return $this;
    }

    public function getCeratedAt(): ?\DateTimeImmutable
    {
        return $this->cerated_At;
    }

    public function setCeratedAt(\DateTimeImmutable $cerated_At): static
    {
        $this->cerated_At = $cerated_At;

        return $this;
    }

    public function getMontant(): ?string
    {
        return $this->montant;
    }

    public function setMontant(string $montant): static
    {
        $this->montant = $montant;

        return $this;
    }
}
